package SS01_Introduction_to_Java.thuc_hanh;

public class Main {
    public static void main(String[] args) {
        int i = 10; //biến i là kiểu int số nguyên có giá trị 10
        float f = 20.5f; //biến f là kiểu float số thực có giá trị 20.5
        double d = 20.5; //biến d là kiểu bouble số thực lớn có giá trị 20.5
        boolean b = true; //biến b là kiểu boolean logic có giá trị true
        char c = 'a'; //biến c là kiểu char ký tự có giá trị là 'a'
        String s = "Hà Nội"; //biến s là kiểu String chuỗi có giá trị là "Hà Nội"
        System.out.println("i = "+i);
        System.out.println("f = "+f);
        System.out.println("d = "+d);
        System.out.println("b = "+b);
        System.out.println("c = "+c);
        System.out.println("s = "+s);
    }
}

